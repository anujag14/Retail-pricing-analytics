DESCRIBE TABLE SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML;

--1. Previewing the data
SELECT *
FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML
LIMIT 10;

--2. Evaluating Model Performance
--Mean Absolute Error (MAE)
SELECT 
    ROUND(AVG(ABS(ACTUAL - LINEAR_REGRESSION_PRED)), 4) AS MAE_LINEAR_REGRESSION,
    ROUND(AVG(ABS(ACTUAL - RANDOM_FOREST_PRED)), 4) AS MAE_RANDOM_FOREST,
    ROUND(AVG(ABS(ACTUAL - GRADIENT_BOOSTING_PRED)), 4) AS MAE_GRADIENT_BOOSTING,
    ROUND(AVG(ABS(ACTUAL - XGBOOST_PRED)), 4) AS MAE_XGBOOST
FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML;

--Root Mean Squared Error (RMSE)
SELECT 
    ROUND(SQRT(AVG(POWER(ACTUAL - LINEAR_REGRESSION_PRED, 2))), 4) AS RMSE_LINEAR_REGRESSION,
    ROUND(SQRT(AVG(POWER(ACTUAL - RANDOM_FOREST_PRED, 2))), 4) AS RMSE_RANDOM_FOREST,
    ROUND(SQRT(AVG(POWER(ACTUAL - GRADIENT_BOOSTING_PRED, 2))), 4) AS RMSE_GRADIENT_BOOSTING,
    ROUND(SQRT(AVG(POWER(ACTUAL - XGBOOST_PRED, 2))), 4) AS RMSE_XGBOOST
FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML;

--Coefficient of Determination (R²)
WITH avg_actual AS (
    SELECT *, AVG(ACTUAL) OVER () AS MEAN_ACTUAL
    FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML
)
SELECT 
    ROUND(1 - (
        SUM(POWER(ACTUAL - LINEAR_REGRESSION_PRED, 2)) /
        SUM(POWER(ACTUAL - MEAN_ACTUAL, 2))
    ), 4) AS R2_LINEAR_REGRESSION,
    
    ROUND(1 - (
        SUM(POWER(ACTUAL - RANDOM_FOREST_PRED, 2)) /
        SUM(POWER(ACTUAL - MEAN_ACTUAL, 2))
    ), 4) AS R2_RANDOM_FOREST,
    
    ROUND(1 - (
        SUM(POWER(ACTUAL - GRADIENT_BOOSTING_PRED, 2)) /
        SUM(POWER(ACTUAL - MEAN_ACTUAL, 2))
    ), 4) AS R2_GRADIENT_BOOSTING,
    
    ROUND(1 - (
        SUM(POWER(ACTUAL - XGBOOST_PRED, 2)) /
        SUM(POWER(ACTUAL - MEAN_ACTUAL, 2))
    ), 4) AS R2_XGBOOST
FROM avg_actual;

--Identifying the best model
WITH metrics AS (
    SELECT 'Linear Regression' AS model,
           AVG(ABS(ACTUAL - LINEAR_REGRESSION_PRED)) AS mae
    FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML

    UNION ALL
    SELECT 'Random Forest',
           AVG(ABS(ACTUAL - RANDOM_FOREST_PRED))
    FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML

    UNION ALL
    SELECT 'Gradient Boosting',
           AVG(ABS(ACTUAL - GRADIENT_BOOSTING_PRED))
    FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML

    UNION ALL
    SELECT 'XGBoost',
           AVG(ABS(ACTUAL - XGBOOST_PRED))
    FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML
)
SELECT *
FROM metrics
ORDER BY mae;

--4. Comparing Predictions with Actual Values
SELECT 
    ACTUAL,
    LINEAR_REGRESSION_PRED,
    RANDOM_FOREST_PRED,
    GRADIENT_BOOSTING_PRED,
    XGBOOST_PRED
FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML
ORDER BY ACTUAL DESC
LIMIT 20;

--5. Creating a View for Dashboards
CREATE OR REPLACE VIEW SUPERMARKET_FORECASTING.PUBLIC.MODEL_COMPARISON AS
SELECT
    ACTUAL,
    LINEAR_REGRESSION_PRED,
    RANDOM_FOREST_PRED,
    GRADIENT_BOOSTING_PRED,
    XGBOOST_PRED,
    ABS(ACTUAL - LINEAR_REGRESSION_PRED) AS ERROR_LR,
    ABS(ACTUAL - RANDOM_FOREST_PRED) AS ERROR_RF,
    ABS(ACTUAL - GRADIENT_BOOSTING_PRED) AS ERROR_GB,
    ABS(ACTUAL - XGBOOST_PRED) AS ERROR_XGB
FROM SUPERMARKET_FORECASTING.PUBLIC.PREDICTIONS_ML;

